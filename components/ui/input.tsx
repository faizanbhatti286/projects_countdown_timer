import * as React from "react";
import { cn } from "@/lib/utils";

export interface InputProps extends React.InputHTMLAttributes<HTMLInputElement> {}

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type = "text", ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          "flex h-10 w-full rounded-md border text-sm px-3 py-2 transition-all duration-300 ease-in-out placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-primary-500 hover:ring-2 hover:ring-primary-300",
          "bg-white border-gray-300 hover:border-primary-500 focus:border-primary-500",
          className
        )}
        ref={ref}
        {...props}
      />
    );
  }
);

Input.displayName = "Input";

export { Input };
